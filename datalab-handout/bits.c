/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
   int a,b,c;
   a = x; 
   b = y; 
   c = a+b;
   a = a >> 31;
   //sign bit extraction for a
   b = b >> 31;
   //sign bit extraction for b
   c = c >> 31;
   //sign bit extraction for c
   return !!(a^b)|(!(a^c)&!(b^c));
   //return whether or not overflow has occured by analyzing if they are the same size and their sum doesn't have the sign that it should and if they are not the same size, then there is no chance of overflow.  
}
/* 
 * bitMask - Generate a mask consisting of all 1's 
 *   lowbit and highbit
 *   Examples: bitMask(5,3) = 0x38
 *   Assume 0 <= lowbit <= 31, and 0 <= highbit <= 31
 *   If lowbit > highbit, then mask should be all 0's
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int bitMask(int highbit, int lowbit) {
    int i, high, low;
    i = ~0;
    //make all bits in i = 1, so i == -1
    high = ~(i << highbit << 1);
    //make all the bits up to highest bit = 1 
    low = i << lowbit;
    //make all the bits up to te left of lowbit = 1
    return high & low;
    //return the bitwise and of these two variables
}
/* 
 * absVal - absolute value of x
 *   Example: absVal(-1) = 1.
 *   You may assume -TMax <= x <= TMax
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 10
 *   Rating: 4
 */
int absVal(int x) {
    int s, mask;
    s = x >> 31;
    //extract the sign bit
    mask = x ^ s;
    //xOr in order to account for negatives
    s = ~s;
    mask = mask + s + 1;
    // complement s to be able to add one to the negatives while canceling out for the positves
    return mask;
}
