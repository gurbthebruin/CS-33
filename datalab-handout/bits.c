/*
 * isZero - returns 1 if x == 0, and 0 otherwise 
 *   Examples: isZero(5) = 0, isZero(0) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 2
 *   Rating: 1
 */
int isZero(int x) {
  return !x;
  //if x == 0, the not will make it nonzero and return true, if x != 0, the not will flip it to 0 and it will return false
}
/* 
 * getByte - Extract byte n from word x
 *   Bytes numbered from 0 (LSB) to 3 (MSB)
 *   Examples: getByte(0x12345678,1) = 0x56
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int getByte(int x, int n) {
    int shifter,shifted_x;
    shifter = n<<3;
    //multiply the n value by 8 to get the shifter value
    shifted_x = x >> shifter;
    //right shift x by the shifter to isolate the desire byte
    return shifted_x & 0xFF;
    // return the shifted val & 0xFF, which isolates the desired byte
    //inspired by example given in class
}
